/**
 * UseCase1HotelBookingApp
 *
 * This class represents the entry point of the Book My Stay
 * Hotel Booking Management System. It demonstrates how a Java
 * application begins execution using the main() method and
 * prints a welcome message to the console.
 *
 * The purpose of this use case is to establish a clear starting
 * point for the application and illustrate basic Java concepts
 * such as class structure, static methods, and console output.
 *
 * @author Oindri
 * @version 1.0
 */

public class UseCase1HotelBookingApp {

    /**
     * Main method – entry point of the Java application.
     * The JVM starts execution from this method.
     *
     * @param args Command line arguments
     */
    public static void main(String[] args) {

        // Display welcome message
        System.out.println("=====================================");
        System.out.println("     Welcome to Book My Stay App     ");
        System.out.println("   Hotel Booking Management System   ");
        System.out.println("           Version 1.0               ");
        System.out.println("=====================================");

        // Inform the user that the application has started successfully
        System.out.println("Application started successfully.");
        System.out.println("Thank you for using Book My Stay!");
    }
}